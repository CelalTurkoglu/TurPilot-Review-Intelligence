def normalize_text(text: object) -> str:
    """Normalize text for rule checks while preserving Turkish characters."""
    return re.sub(r"\s+", " ", "" if pd.isna(text) else str(text).lower()).strip()


def phrase_pattern(phrase: str) -> str:
    """Build a Unicode-aware regex pattern for exact phrase matching."""
    escaped = re.escape(phrase)
    prefix = r"(?<!\w)"
    suffix = "" if phrase in STEM_TERMS else r"(?!\w)"
    return f"{prefix}{escaped}{suffix}"


def count_phrase_occurrences(text: str, phrases: list[str]) -> int:
    """Count phrase occurrences in a normalized text window."""
    return sum(len(re.findall(phrase_pattern(phrase), text)) for phrase in phrases)


def find_phrase_positions(text: str, phrases: list[str]) -> list[tuple[int, int]]:
    """Find all phrase spans for one aspect vocabulary."""
    positions: list[tuple[int, int]] = []
    for phrase in phrases:
        positions.extend((match.start(), match.end()) for match in re.finditer(phrase_pattern(phrase), text))
    return positions


def extract_aspect_signal(text: object, aspect: str, star_rating: int) -> dict[str, int]:
    """Return rule-assisted aspect sentiment signals and the final label."""
    normalized = normalize_text(text)
    aspect_positions = find_phrase_positions(normalized, ASPECT_TERMS[aspect])

    mentioned = int(bool(aspect_positions))
    positive_score = 0
    negative_score = 0

    # Sentiment is counted near the aspect term to avoid assigning global review
    # sentiment to unrelated categories.
    for start, end in aspect_positions:
        window = normalized[max(0, start - 110) : min(len(normalized), end + 110)]
        positive_score += count_phrase_occurrences(window, POSITIVE_TERMS)
        negative_score += count_phrase_occurrences(window, NEGATIVE_TERMS)

    # If a review only expresses a general tour/company opinion, it belongs to
    # the organization aspect instead of being copied to all five categories.
    if aspect == "Organizasyon" and not aspect_positions:
        positive_score += count_phrase_occurrences(normalized, GENERAL_ORGANIZATION_POSITIVE)
        negative_score += count_phrase_occurrences(normalized, GENERAL_ORGANIZATION_NEGATIVE)
        mentioned = int((positive_score + negative_score) > 0)

    for pattern, positive_boost, negative_boost in REGEX_BOOSTERS[aspect]:
        if re.search(pattern, normalized):
            positive_score += positive_boost
            negative_score += negative_boost
            mentioned = 1

    label = 0
    if mentioned:
        # Complaint wins ties because a single concrete service failure should be
        # visible in the automatic response.
        if negative_score > 0 and negative_score >= positive_score:
            label = 2
        elif positive_score > 0:
            label = 1
        elif star_rating >= 4:
            label = 1
        elif star_rating <= 2:
            label = 2

    return {
        "label": label,
        "mentioned": mentioned,
        "pos_score": positive_score,
        "neg_score": negative_score,
        "star_pos": int(mentioned and star_rating >= 4),
        "star_neg": int(mentioned and star_rating <= 2),
    }


def relabel_dataframe(raw_df: pd.DataFrame) -> pd.DataFrame:
    """Create a clean labelled dataset from the raw scraped comments."""
    labelled_df = raw_df[["Yorum", "Yildiz"]].copy()
    labelled_df["Yildiz"] = pd.to_numeric(labelled_df["Yildiz"], errors="coerce").fillna(3).astype(int)
    labelled_df["Yildiz"] = labelled_df["Yildiz"].clip(lower=1, upper=5)

    for aspect in TARGET_COLUMNS:
        labelled_df[aspect] = [
            extract_aspect_signal(review, aspect, int(star_rating))["label"]
            for review, star_rating in zip(labelled_df["Yorum"], labelled_df["Yildiz"])
        ]

    return labelled_df


def print_summary(old_df: pd.DataFrame | None, new_df: pd.DataFrame) -> None:
    """Print a compact before/after audit summary."""
    print(f"Rows: {len(new_df)}")
    print("Star distribution:", new_df["Yildiz"].value_counts().sort_index().to_dict())
    print("\nNew label distribution:")
    for column in TARGET_COLUMNS:
        print(f"  {column}: {new_df[column].value_counts().sort_index().to_dict()}")

    if old_df is not None and all(column in old_df.columns for column in TARGET_COLUMNS):
        print("\nChanged labels compared with previous updated_dataset.csv:")
        changed_any = (old_df[TARGET_COLUMNS].to_numpy() != new_df[TARGET_COLUMNS].to_numpy()).any(axis=1)
        print(f"  Rows changed in at least one category: {int(changed_any.sum())}")
        for column in TARGET_COLUMNS:
            changed_count = int((old_df[column].to_numpy() != new_df[column].to_numpy()).sum())
            print(f"  {column}: {changed_count}")

    combo_count = new_df[TARGET_COLUMNS].astype(str).agg("-".join, axis=1).nunique()
    print(f"\nUnique target combinations: {combo_count}")


def main() -> None:
    """Rebuild and save both project copies of updated_dataset.csv."""
    if not RAW_DATASET_PATH.exists():
        raise FileNotFoundError(f"Raw dataset not found: {RAW_DATASET_PATH}")

    raw_df = pd.read_csv(RAW_DATASET_PATH, encoding="utf-8-sig")
    old_df = pd.read_csv(MODEL_DATASET_PATH, encoding="utf-8-sig") if MODEL_DATASET_PATH.exists() else None

    labelled_df = relabel_dataframe(raw_df)
    MODEL_DATASET_PATH.parent.mkdir(parents=True, exist_ok=True)
    SCRAPING_DATASET_PATH.parent.mkdir(parents=True, exist_ok=True)

    # utf-8-sig keeps Turkish characters safe when the CSV is opened in Excel.
    labelled_df.to_csv(MODEL_DATASET_PATH, index=False, encoding="utf-8-sig")
    labelled_df.to_csv(SCRAPING_DATASET_PATH, index=False, encoding="utf-8-sig")

    print_summary(old_df, labelled_df)
    print(f"\nSaved: {MODEL_DATASET_PATH}")
    print(f"Saved: {SCRAPING_DATASET_PATH}")


if __name__ == "__main__":
    main()
