def normalize_text(text: object) -> str:
    """Normalize text for rule checks while preserving Turkish characters."""
    normalized = "" if pd.isna(text) else str(text).lower()
    # Python lowercases "İ" to "i" + combining dot; collapse it for matching.
    normalized = normalized.replace("i̇", "i")
    return re.sub(r"\s+", " ", normalized).strip()


def normalize_review_for_dedupe(text: object) -> str:
    """Normalize review text to remove duplicate scraped comments."""
    normalized = "" if pd.isna(text) else str(text)
    normalized = unicodedata.normalize("NFKC", normalized)
    normalized = normalized.casefold()
    return re.sub(r"\s+", " ", normalized).strip()


def phrase_pattern(phrase: str) -> str:
    """Build a Unicode-aware regex pattern for exact phrase matching."""
    escaped = re.escape(phrase)
    prefix = r"(?<!\w)"
    suffix = "" if phrase in STEM_TERMS else r"(?!\w)"
    return f"{prefix}{escaped}{suffix}"


def count_phrase_occurrences(text: str, phrases: list[str]) -> int:
    """Count phrase occurrences in a normalized text window."""
    return sum(len(re.findall(phrase_pattern(phrase), text)) for phrase in phrases)


def find_phrase_positions(text: str, phrases: list[str]) -> list[tuple[int, int]]:
    """Find all phrase spans for one aspect vocabulary."""
    positions: list[tuple[int, int]] = []
    for phrase in phrases:
        positions.extend((match.start(), match.end()) for match in re.finditer(phrase_pattern(phrase), text))
    return positions


def extract_aspect_signal(text: object, aspect: str, star_rating: int) -> dict[str, int]:
    """Return rule-assisted aspect sentiment signals and the final label."""
    normalized = normalize_text(text)
    aspect_positions = find_phrase_positions(normalized, ASPECT_TERMS[aspect])

    mentioned = int(bool(aspect_positions))
    positive_score = 0
    negative_score = 0

    # Sentiment is counted near the aspect term to avoid assigning global review
    # sentiment to unrelated categories.
    for start, end in aspect_positions:
        window = normalized[max(0, start - 110) : min(len(normalized), end + 110)]
        positive_score += count_phrase_occurrences(window, POSITIVE_TERMS)
        negative_score += count_phrase_occurrences(window, NEGATIVE_TERMS)

    # If a review only expresses a general tour/company opinion, it belongs to
    # the organization aspect instead of being copied to all five categories.
    if aspect == "Organizasyon" and not aspect_positions:
        positive_score += count_phrase_occurrences(normalized, GENERAL_ORGANIZATION_POSITIVE)
        negative_score += count_phrase_occurrences(normalized, GENERAL_ORGANIZATION_NEGATIVE)
        mentioned = int((positive_score + negative_score) > 0)

    for pattern, positive_boost, negative_boost in REGEX_BOOSTERS[aspect]:
        if re.search(pattern, normalized):
            positive_score += positive_boost
            negative_score += negative_boost
            mentioned = 1

    label = 0
    if mentioned:
        # Complaint wins ties because a single concrete service failure should be
        # visible in the automatic response.
        if negative_score > 0 and negative_score >= positive_score:
            label = 2
        elif positive_score > 0:
            label = 1
        elif star_rating >= 4:
            label = 1
        elif star_rating <= 2:
            label = 2

    return {
        "label": label,
        "mentioned": mentioned,
        "pos_score": positive_score,
        "neg_score": negative_score,
        "star_pos": int(mentioned and star_rating >= 4),
        "star_neg": int(mentioned and star_rating <= 2),
    }


